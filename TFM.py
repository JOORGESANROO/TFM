import pandas as pd
import yfinance as yf
import matplotlib.pyplot as plt
import seaborn as sns

# Configuraci√≥n gr√°fica
sns.set_style("whitegrid")
plt.rcParams["figure.figsize"] = (12,6)

# ===================== #
#   PERIODO DE AN√ÅLISIS
# ===================== #
start_date = "2024-11-05"
end_date = "2025-04-30"

# ===================== #
#   1. √çNDICES SECTORIALES
# ===================== #
sectores_tickers = {
    "Tecnolog√≠a": "XLK",
    "Salud": "XLV",
    "Finanzas": "XLF",
    "Energ√≠a": "XLE"
}

sectores = {}
for sector, ticker in sectores_tickers.items():
    df = yf.download(ticker, start=start_date, end=end_date)["Close"].dropna()
    df_return = df.pct_change()
    df_cum = (1 + df_return).cumprod() * 100
    sectores[sector] = {"Close": df, "Return": df_return, "Cumulative Return": df_cum}
    print(f"‚úÖ {sector} - √öltimos 5 valores Close")
    print(df.tail(), "\n")
    print(f"{sector} - √öltimos 5 valores Return")
    print(df_return.tail(), "\n")
    print(f"{sector} - √öltimos 5 valores Cumulative Return")
    print(df_cum.tail(), "\n")

    # Gr√°fico sector
    plt.figure()
    plt.plot(df.index, df, label=f"{sector} Close")
    plt.plot(df_cum.index, df_cum, label=f"{sector} Cumulative", linestyle="--")
    plt.title(f"Evoluci√≥n {sector}")
    plt.xlabel("Fecha")
    plt.ylabel("Valor")
    plt.legend()
    plt.show()

